export ESC="\e"

export RED="${ESC}[31m"
export GREEN="${ESC}[32m"
export YELLOW="${ESC}[33m"
export MAGENTA="${ESC}[35m"
export CYAN="${ESC}[36m"
export LGRAY="${ESC}[37m"

export BOLD="${ESC}[1m"
export RST="${ESC}[0m"

export P_RED="\[${RED}\]"
export P_GREEN="\[${GREEN}\]"
export P_YELLOW="\[${YELLOW}\]"
export P_MAGENTA="\[${MAGENTA}\]"
export P_CYAN="\[${CYAN}\]"
export P_LGRAY="\[${LGRAY}\]"

export P_BOLD="\[${BOLD}\]"
export P_RST="\[${RST}\]"
